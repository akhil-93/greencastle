<h2>Wayanad Tourism</h2>
<p>Wayanad District is a district in the north-east of Kerala state, India with headquarters at the town of Kalpetta. The district was formed on 1 November 1980 as the 12th district in Kerala by carving out areas from Kozhikode and Kannur districts. The district is 3.79% urbanised, with only one municipal town of Kalpetta.

Wayanad district is in the southern tip of the Deccan Plateau. Part of the Western Ghats is in the district. In the centre of the district hills are lower in height while the northern area has high hills. The eastern area is flat and open.<br><br>

Wayanad is the only district in Kerala that borders both the neighboring states Karnataka and Tamil Nadu. Wayanad borders Kozhikode, Kannur, and Malappuram districts in Kerala; Nilgiris district in Tamil Nadu; and Chamarajanagar, Mysore, and Kodagu (Coorg) districts in Karnataka.
</p>

<h3>Tourism</h3>
<p>The natural scenic beauty of Wayanad and its rich natural resources offer several opportunities for adventure tourism. The hills rocks and valley which make the very unique character of this region provide a lot for catering to the ever increasingdemand for adventure tourist.</p>

<h3>Climate</h3>
<p>Wayanad has a salubrious climate. The mean average rain fall in this district is 2322 m.m. Lakkidi, Vythiri and Meppadi are the high rainfall areas in Wayanad. Annual rain fall in these high rain fall areas ranges from 3,000to 4,000m.m. High velocity
winds are common during the south west monsoon and dry winds blow in March-April. High altitude regions experience severe cold. In Wayanad (Ambalavayal) the mean maximum and minimum temperature for the last five years were 29oC and 18oC respectively. This place experiences a high relativehumidity which goes even up to 95 per cent during the south west monsoon period.</p>

<h3>Wayanad Wildlife Sanctuary</h3>
<p>Wayanad Wildlife Sanctuary is made up of two discontinuous pockets â€“ Muthanga and Tholpetty. While Muthanga is situated to the south of Wayanad, about 18 km from Sulthan Bathery, Tholpetty is located towards the north of the district adjacent to Thirunelli. Both these pockets together cover an area of 344.44 sq km and are known for their rugged wild charm. Cocooned in the Nilgiri Biosphere, Muthanga lies adjacent to Bandipur Tiger Reserve in Karnataka and Mudumalai Wildlife Sanctuary in Tamil Nadu. It also borders Sulthan Bathery and Kurichiat Ranges within Kerala and is known for its rich biodiversity. Tholpetty also lies adjacent to Nagarhole Range of Karnataka.</p>
